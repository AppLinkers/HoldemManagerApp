package com.example.server_test.pub;

public class Pub {
    private String pub_info;
    private String pub_name;
    private String pub_game;
    private String pub_start;
    private String pub_end;

    public String getPub_info() {
        return pub_info;
    }

    public void setPub_info(String pub_info) {
        this.pub_info = pub_info;
    }

    public String getPub_name() {
        return pub_name;
    }

    public void setPub_name(String pub_name) {
        this.pub_name = pub_name;
    }

    public String getPub_game() {
        return pub_game;
    }

    public void setPub_game(String pub_game) {
        this.pub_game = pub_game;
    }

    public String getPub_start() {
        return pub_start;
    }

    public void setPub_start(String pub_start) {
        this.pub_start = pub_start;
    }

    public String getPub_end() {
        return pub_end;
    }

    public void setPub_end(String pub_end) {
        this.pub_end = pub_end;
    }
}
